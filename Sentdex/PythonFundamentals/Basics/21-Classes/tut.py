
class calculator:
    def addition(x,y):
        added = x + y
        print("The addition of", x, "and", y, "is", added, ".")

    def subtraction(x,y):
        subtracted = x - y
        print("The subtraction of", x, "and", y, "is", subtracted, ".")

    def multiplication(x,y):
        multiplied = x * y
        print("The multiplication of", x, "and", y, "is", multiplied, ".")

    def division(x,y):
        divided = x / y
        print("The division of", x, "and", y, "is", divided, ".")

calculator.addition(7,7)
calculator.subtraction(7,7)
calculator.multiplication(7,7)
calculator.division(7,7)
